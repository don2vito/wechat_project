"""
验证数据可视化准确性的脚本
核心功能：对多区域年度数据的可视化结果进行全维度验证，确保图表展示的数值、增长率、颜色等与原始数据一致
适用场景：数据可视化后的数据准确性校验，避免图表展示错误
"""

# 导入pandas库（数据处理核心库，用于构建和操作DataFrame表格数据）
import pandas as pd
# 导入numpy库（数值计算库，本脚本未直接使用，但属于数据处理常用库，保留便于扩展）
import numpy as np

# ===================== 第一步：构建原始数据集 =====================
# 定义各区域2021-2024年的原始数据（字典结构：键=区域名称，值=对应年度数值列表）
data = {
    '华东': [120, 110, 105, 115],  # 华东区域2021、2022、2023、2024年数值
    '华南': [95, 88, 82, 78],      # 华南区域对应年度数值
    '华北': [88, 85, 90, 100],     # 华北区域对应年度数值
    '西南': [76, 82, 70, 80],      # 西南区域对应年度数值
    '西北': [62, 58, 55, 60]       # 西北区域对应年度数值
}
# 定义年份列表（作为DataFrame的行索引，与数据列表长度一一对应）
years = ['2021', '2022', '2023', '2024']
# 构建DataFrame表格（pandas核心数据结构，类似Excel表格）
# 参数说明：data=数据源字典，index=行索引（年份），列索引默认是字典的键（区域）
df = pd.DataFrame(data, index=years)

# ===================== 第二步：输出验证报告头部 =====================
print("=" * 80)  # 打印分隔线，提升报告可读性
print("数据可视化验证报告")  # 报告标题
print("=" * 80)

# ===================== 验证模块1：原始数据验证 =====================
print("\n【1】原始数据验证")  # 验证模块标题
print("-" * 80)  # 模块内部分隔线
print("原始数据:")  # 子标题
print(df)  # 打印完整的DataFrame，核对原始数据是否正确构建

# ===================== 验证模块2：各区域数据标签验证 =====================
print("\n【2】各区域数据标签验证")  # 验证图表中各区域数值标签是否准确
print("-" * 80)
print("各区域数值（应与图表标签一致）:")
# 遍历每一个年份（行索引）
for year in years:
    print(f"\n{year}年:")  # 打印当前年份
    # 遍历每一个区域（列索引）
    for region in df.columns:
        # 按行+列索引取值（df.loc[行标签, 列标签]），打印区域名称和对应数值
        print(f"  {region}: {df.loc[year, region]}")

# ===================== 验证模块3：年度总数值验证 =====================
print("\n【3】年度总数值验证")  # 验证图表中年度总计数值是否准确
print("-" * 80)
# 计算每年的总数值：axis=1 表示按行求和（即每个年份下所有区域数值之和）
# axis=0 表示按列求和（每个区域所有年份数值之和）
totals = df.sum(axis=1)
print("各年度总数值（应与图表顶部标签一致）:")
# 同时遍历年份和对应总数值（zip函数用于多列表并行遍历）
for year, total in zip(years, totals):
    print(f"{year}年: {total}")

# ===================== 验证模块4：总数值一致性验证 =====================
print("\n【4】各区域数值之和与总数值一致性验证")  # 校验求和逻辑是否正确
print("-" * 80)
for year in years:
    # 单独计算当前年份各区域数值之和（df.loc[year]取整行数据，sum()求和）
    sum_regions = df.loc[year].sum()
    # 打印对比结果：手动求和值 vs 之前的总数值，以及是否相等的布尔值
    print(f"{year}年: 各区域之和 = {sum_regions}, 总数值 = {totals[year]}, 一致: {sum_regions == totals[year]}")

# ===================== 验证模块5：环比增长率计算验证 =====================
print("\n【5】环比增长率计算验证")  # 验证图表中环比增长率是否准确
print("-" * 80)
# 初始化空列表，用于存储每年的环比增长率
growth_rates = []
# 遍历总数值（从第2个开始，因为环比需要和上一年对比）
for i in range(1, len(totals)):
    prev_total = totals[i-1]  # 上一年的总数值（i=1时，取i-1=0即2021年）
    curr_total = totals[i]    # 当前年份的总数值（i=1时，取2022年）
    # 环比增长率公式：(本年-上年)/上年 × 100%
    growth_rate = ((curr_total - prev_total) / prev_total) * 100
    growth_rates.append(growth_rate)  # 将计算结果存入列表
    # 打印详细计算过程，保留2位小数，同时展示四舍五入后的整数（图表常用）
    print(f"{years[i-1]} → {years[i]}: ({curr_total} - {prev_total}) / {prev_total} × 100% = {growth_rate:.2f}% (四舍五入: {int(round(growth_rate))}%)")

# ===================== 验证模块6：柱状图高度验证 =====================
print("\n【6】柱状图高度验证")  # 验证堆叠柱状图的总高度是否等于年度总数值
print("-" * 80)
print("各年度柱状图总高度应等于各区域数值之和:")
for year, total in zip(years, totals):
    print(f"{year}年: 总高度 = {total}")

# ===================== 验证模块7：颜色组合验证 =====================
print("\n【7】颜色组合验证")  # 验证图表中各区域的颜色编码是否正确
print("-" * 80)
# 定义各区域对应的十六进制颜色码（图表常用配色）
colors = ['#3498db', '#2ecc71', '#e74c3c', '#f39c12', '#9b59b6']
# 区域列表（需与colors列表长度、顺序完全一致）
regions = ['华东', '华南', '华北', '西南', '西北']
print("区域颜色对应关系（应与图表一致）:")
# 遍历区域和对应颜色，核对配色是否正确
for region, color in zip(regions, colors):
    print(f"  {region}: {color}")

# ===================== 验证总结 =====================
print("\n" + "=" * 80)
print("验证总结")  # 报告总结部分，提炼核心验证结果
print("=" * 80)
print("\n预期结果:")
# 总结1：数据标签验证结果
print("1. 数据标签: 各区域数值与原始数据一致 ✓")
# 总结2：年度总数值汇总
print("2. 总数值:")
for year, total in zip(years, totals):
    print(f"   {year}年: {total}")
# 总结3：环比增长率汇总（取整后，与图表展示一致）
print("3. 环比增长率:")
for i, rate in enumerate(growth_rates):
    print(f"   {years[i]}→{years[i+1]}: {int(round(rate))}%")
# 总结4：颜色对应关系验证结果
print("4. 颜色组合: 华东(蓝), 华南(绿), 华北(红), 西南(橙), 西北(紫) ✓")

print("\n" + "=" * 80)
